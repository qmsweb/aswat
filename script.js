// دالة البحث الرئيسية التي يطلبها الـ HTML
function searchPhonetic() {
    // 1. جلب المدخل وتنظيفه من المسافات
    let inputField = document.getElementById('charInput');
    let rawInput = inputField.value.trim();
    
    // 2. إزالة التشكيل (الحركات) إذا أدخلها المستخدم بالخطأ
    let cleanInput = rawInput.replace(/[\u064B-\u065F]/g, '');

    let resultSection = document.getElementById('resultSection');
    let resultDetails = document.getElementById('resultDetails');

    // التحقق من أن الحقل ليس فارغاً
    if (cleanInput === "") {
        inputField.focus();
        return;
    }

    // 3. توحيد أشكال الهمزة لتطابق حرف "أ" في البيانات
    if (['أ', 'إ', 'آ', 'ء', 'ؤ', 'ئ'].includes(cleanInput)) {
        cleanInput = 'أ';
    }

    // 4. جلب بيانات الحرف من المصفوفة
    let charData = phoneticsData[cleanInput];

    // إظهار قسم النتيجة
    resultSection.style.display = 'block';

    if (charData) {
        // تعبئة البيانات في حال وجود الحرف
        document.getElementById('charDisplay').innerText = cleanInput;
        document.getElementById('charName').innerText = charData["التسمية"];
        
        resultDetails.innerHTML = `
            <div class="detail-card">
                <div class="detail-title">المخرج</div>
                <div class="detail-value">${charData["المخرج"]}</div>
            </div>
            <div class="detail-card">
                <div class="detail-title">طريقة النطق</div>
                <div class="detail-value">${charData["طريقة النطق"]}</div>
            </div>
            <div class="detail-card">
                <div class="detail-title">اهتزاز الوترين</div>
                <div class="detail-value">${charData["اهتزاز الوترين"]}</div>
            </div>
            <div class="detail-card">
                <div class="detail-title">شكل اللسان</div>
                <div class="detail-value">${charData["شكل اللسان"]}</div>
            </div>
            <div class="detail-card">
                <div class="detail-title">الملخَّص</div>
                <div class="detail-value">${charData["ملخَّص"]}</div>
            </div>
        `;
    } else {
        // في حال إدخال حرف غير موجود (مثل أحرف إنجليزية أو أرقام)
        document.getElementById('charDisplay').innerText = "؟";
        document.getElementById('charName').innerText = "عذراً، لم يتم العثور على النتيجة";
        resultDetails.innerHTML = `<div class="error-message">الرجاء إدخال حرف عربي صحيح من الصوامت.</div>`;
    }
}

// دالة فتح وإغلاق القائمة الجانبية (مطلوبة أيضاً في HTML الخاص بك)
function toggleMenu() {
    const sideMenu = document.getElementById('sideMenu');
    const overlay = document.getElementById('overlay');
    
    if(sideMenu && overlay) {
        sideMenu.classList.toggle('show');
        overlay.classList.toggle('show');
    }
}

// إغلاق القائمة عند النقر على الخلفية المعتمة
document.getElementById('overlay').addEventListener('click', toggleMenu);
